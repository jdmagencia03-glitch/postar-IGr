export const PLAYBOOK_PRESETS = {
  deolhonoshape_fitness_dark: buildDeOlhoNoShapePlaybookPayload,
  retr0fy_retro_games: buildRetr0fyRetroGamesPlaybookPayload,
  diario_da_musa_feminino: buildDiarioDaMusaPlaybookPayload,
} as const;

export type PlaybookPresetId = keyof typeof PLAYBOOK_PRESETS;

const ACCOUNT_PRESET_MAP: Record<string, PlaybookPresetId> = {
  "dea1e690-f378-417d-8654-770974677fed": "deolhonoshape_fitness_dark",
  "c7b40b69-5377-46f5-abe7-950b60efd05c": "retr0fy_retro_games",
  "845efd12-14f4-4d85-8a0c-63adcb898830": "diario_da_musa_feminino",
};

export function defaultPresetForAccount(accountId: string): PlaybookPresetId | null {
  return ACCOUNT_PRESET_MAP[accountId] ?? null;
}

function trimOrNull(value: string | null | undefined, max: number) {
  const trimmed = value?.trim();
  if (!trimmed) return null;
  return trimmed.slice(0, max);
}

export function normalizePlaybookPayload(
  partial: Partial<AccountPlaybookPayload>,
  playbookName?: string | null,
): AccountPlaybookPayload {
  return {
    brand_name: trimOrNull(playbookName ?? partial.brand_name, 200),
    niche: trimOrNull(partial.niche, 500),
    target_audience: trimOrNull(partial.target_audience, 3000),
    tone_voice: trimOrNull(partial.tone_voice, 2000),
    viral_hooks: trimOrNull(partial.viral_hooks, 5000),
    hashtag_strategy: trimOrNull(partial.hashtag_strategy, 3000),
    cta_style: trimOrNull(partial.cta_style, 2000),
    example_captions: trimOrNull(partial.example_captions, 8000),
    avoid_rules: trimOrNull(partial.avoid_rules, 2000),
    extra_knowledge: trimOrNull(partial.extra_knowledge, 15000),
  };
}

export function resolvePlaybookPayload(params: {
  accountId: string;
  preset?: PlaybookPresetId | null;
  playbookName?: string | null;
  playbook?: Partial<AccountPlaybookPayload> | null;
}): AccountPlaybookPayload {
  const presetId = params.preset ?? defaultPresetForAccount(params.accountId);
  const base = presetId ? PLAYBOOK_PRESETS[presetId]() : {};
  const merged = { ...base, ...(params.playbook ?? {}) };
  return normalizePlaybookPayload(merged, params.playbookName);
}
